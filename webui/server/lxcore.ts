/**
 * Parse configuration file content
 */
export function parseConfigContent(content: string): ConfigValue[] {
  const lines = content.split('\n');
  const config: ConfigValue[] = [];

  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;

    const [key, value] = trimmed.split('=');
    if (key && value !== undefined) {
      config.push({
        key: key.trim(),
        value: value.trim(),
      });
    }
  }

  return config;
}

/**
 * Generate updated configuration content
 */
export function generateConfigContent(config: ConfigValue[]): string {
  let content = '# Lynx Configuration\n';
  content += '# Auto-generated by WebUI\n\n';

  for (const item of config) {
    content += `${item.key}=${item.value}\n`;
  }

  return content;
}

/**
 * Validate configuration value
 */
export function validateConfigValue(key: string, value: string): { valid: boolean; error?: string } {
  // Validate known configuration keys
  const validKeys: Record<string, { type: string; pattern?: RegExp }> = {
    'lynx.mode': { type: 'enum', pattern: /^(notset|auto|aggressive|high|powersave)$/ },
    'lynx.thermal': { type: 'enum', pattern: /^(notset|0|1)$/ },
    'lynx.control': { type: 'enum' },
    'lynx.cc': { type: 'number', pattern: /^\d+$/ },
    'lynx.fcc': { type: 'string' },
    'lynx.lcc': { type: 'number', pattern: /^[\d.]+$/ },
    'lynx.ac': { type: 'string' },
    'lynx.max.ac': { type: 'number', pattern: /^\d+$/ },
    'lynx.min.ac': { type: 'number', pattern: /^\d+$/ },
    'lynx.flow': { type: 'enum', pattern: /^(notset|0|1)$/ },
    'flow.mode': { type: 'enum', pattern: /^(notset|0|1|2|3|5)$/ },
    'window': { type: 'string' },
    'trans': { type: 'string' },
    'anim': { type: 'string' },
  };

  if (!validKeys[key]) {
    return { valid: false, error: `Unknown configuration key: ${key}` };
  }

  const keyConfig = validKeys[key];
  if (keyConfig.pattern && !keyConfig.pattern.test(value)) {
    return { valid: false, error: `Invalid value for ${key}: ${value}` };
  }

  return { valid: true };
}

