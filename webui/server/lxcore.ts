/**
 * Parse Lxcore command output
 */
export function parseLxcoreOutput(output: string): { success: boolean; message: string } {
  const lines = output.split('\n');
  let success = true;
  let message = '';

  for (const line of lines) {
    if (line.includes('Error') || line.includes('error') || line.includes('❌')) {
      success = false;
    }
    if (line.includes('✅') || line.includes('Success')) {
      success = true;
    }
    message += line + '\n';
  }

  return { success, message: message.trim() };
}

