/**
 * Parse system metrics from /proc files
 */
export function parseSystemMetrics(procData: Record<string, string>): SystemMetrics {
  const metrics: SystemMetrics = {
    cpu: {
      usage: 0,
      cores: 0,
      frequency: [],
      governors: [],
    },
    gpu: {
      frequency: '0',
      load: 0,
    },
    ram: {
      total: 0,
      available: 0,
      used: 0,
      percentage: 0,
    },
    thermal: {
      temperature: 0,
      throttling: false,
      zones: {},
    },
    zram: {
      size: 0,
      used: 0,
      algorithm: 'lz4',
    },
  };

  // Parse CPU info
  if (procData.cpuinfo) {
    metrics.cpu.cores = (procData.cpuinfo.match(/processor/g) || []).length;
  }

  // Parse CPU frequency
  if (procData.cpuFreq) {
    metrics.cpu.frequency = [String(parseInt(procData.cpuFreq) / 1000)];
  }

  // Parse CPU governor
  if (procData.cpuGov) {
    metrics.cpu.governors = [procData.cpuGov.trim()];
  }

  // Parse RAM metrics
  if (procData.meminfo) {
    const memLines = procData.meminfo.split('\n');
    for (const line of memLines) {
      if (line.startsWith('MemTotal:')) {
        metrics.ram.total = parseInt(line.split(/\s+/)[1]) * 1024;
      }
      if (line.startsWith('MemAvailable:')) {
        metrics.ram.available = parseInt(line.split(/\s+/)[1]) * 1024;
      }
    }
    metrics.ram.used = metrics.ram.total - metrics.ram.available;
    metrics.ram.percentage = (metrics.ram.used / metrics.ram.total) * 100;
  }

  // Parse thermal metrics
  if (procData.thermal) {
    metrics.thermal.temperature = parseInt(procData.thermal) / 1000;
  }

  // Parse ZRAM metrics
  if (procData.zramSize) {
    metrics.zram.size = parseInt(procData.zramSize);
  }

  if (procData.zramStat) {
    const stats = procData.zramStat.split(/\s+/);
    metrics.zram.used = parseInt(stats[2]) || 0;
  }

  return metrics;
}

